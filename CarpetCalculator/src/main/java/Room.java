import java.util.ArrayList;

public class Room {
    //
    //Data Members
    //
    private int width;
    private int length;

    //
    //Constructor
    //
    public Room(int width, int length) {
        this.width = width;
        this.length = length;
    }
    //
    //Accesors
    //
    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }
}
